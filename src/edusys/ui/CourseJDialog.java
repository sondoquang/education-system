    @Override
    public void updateDiem() {
        try {
            for(int i=0 ; i< tblHocVien.getRowCount() ; i++){
                int mahv = Integer.parseInt(tblHocVien.getValueAt(i, 0)+"");
                Double diem = Double.valueOf(tblHocVien.getValueAt(i, 5)+"");
                if(XCheck.checkMark(diem)){
                    HocVien hv = new HocVien();
                    hv.setDiem(diem);
                    hv.setMaHV(mahv);
                    HVDAO.update(hv);
                }else{
                    XMsgBox.alert(null,"Sai định dạng điểm.");
                    return;
                }
            }   
            this.fillToTableHocVien();
            XMsgBox.alert(null,"Cập nhật thành công.");
        } catch (Exception e) {
            XMsgBox.alert(null,"Sai định dạng điểm. !!");
        }
        
    }

