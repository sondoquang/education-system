    @Override
    public void removeHocVien() {
        if(!Auth.isManager()){
            XMsgBox.alert(null,"Bạn không có quyền xóa !!");
        }else {
            if(XMsgBox.confirm(null,"Bạn muốn xóa các học viên được chọn ?")){
                try {
                    for(int rows: tblHocVien.getSelectedRows()){
                    int mahv = Integer.parseInt(tblHocVien.getValueAt(rows, 0)+"");
                        HVDAO.delete(mahv);
                    }
                    XMsgBox.alert(null,"Xóa học viên thành công.");
                } catch (Exception e) {
                    XMsgBox.alert(null,"Xóa học viên thất bại !!");
                }
                this.fillToTableHocVien();
                this.fillToTableNguoiHoc();
            }
        }
    }
